def distancia(punto1, punto2):
    return ((punto2[0] - punto1[0]) ** 2 + (punto2[1] - punto1[1]) ** 2) ** 0.5

# Ejemplo de uso
resultado = distancia((3, 4), (7, 1))
print("La distancia entre los puntos es:", resultado)
